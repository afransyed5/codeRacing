	@Test
	public void testJustify() {
		Assert.assertArrayEquals(new String[] { "b" },
				wrap.justify(new String[] { "b" }, 2));

		Assert.assertArrayEquals(
				new String[] { "th e", "ow l", "an d", "th e", "pu", "ss",
						"yc", "at", "we", "nt", "to", "se a", "on a", "be",
						"au", "ti", "fu l", "pe a", "gr", "ee n", "bo", "at" },
				wrap.justify(new String[] { "th e ow l an d th e pu ss yc at",
						"we nt to se a",
						"on a be au ti fu l pe a gr ee n bo at" }, 4));

		Assert.assertArrayEquals(
				new String[] { "now is the time for", "all good men to come",
						"to the aid of their", "country" },
				wrap.justify(new String[] { "now", "is the time for", "all",
						"good", "men", "to", "come to the aid", "of", "their",
						"country" }, 20));

		Assert.assertArrayEquals(
				new String[] { "now is the time for", "all good men to come",
						"to the aid of their", "country" },
				wrap.justify(new String[] { "now is the time for all good men",
						"to come to the aid of their country" }, 20));

		Assert.assertArrayEquals(
				new String[] {
						"four score and seven years ago our fathers brought",
						"forth upon this continent a new nation conceived",
						"in liberty and dedicated to the proposition that",
						"all men are created equal" },
				wrap.justify(new String[] { "four score and seven years ago",
						"our fathers brought forth", "upon this continent",
						"a new nation", "conceived in liberty",
						"and dedicated to the proposition",
						"that all men are created", "equal" }, 50));
	}

